package Firstparts;

public class Arithmeticoperator {
    public static void main(String[] args){
        int a=10,b=5,c;
        c=a+b;
        System.out.println(c);
        c=a*b;
        System.out.println(c);
        c=a/b;
        System.out.println(c);
        c=a%b;
        System.out.println(c);
        c+=2;
        System.out.println(c);
        c-=2;
        System.out.println(c);
        c/=2;
        System.out.println(c);
        c%=2;
        System.out.println(c);
        c++;
        System.out.println(c);
        c--;
        System.out.println(c);
    }
}
